%% Extract Rooms and Bees information   
[mRow,mCol]=size(theMaze);
mazeRooms=theMaze;
for i=1:mRow
    for j=1:mCol
        mazeRooms{i,j}(3)='0';
        mazeBees(i,j)=theMaze{i,j}(3);
    end
end

%% Draw the maze
MazeRow=[];
Maze=[];
 for row=1:mRow
     for col=1:mCol
         roomNumber=mazeRooms(row,col);
         roomTile= imread(char(strcat(roomNumber,".png")));
         if mazeBees(row,col)~="0" % If there are bees draw them
             Bees=imread(char(strcat(mazeBees(row,col),".png")));
             NegBees=uint8(~(Bees>0));
             roomTile=roomTile.*NegBees+Bees;
         end
         MazeRow=[MazeRow,roomTile];
     end
         Maze=[Maze;MazeRow];
         MazeRow=[];
 end
imwrite(Maze,'MazePrint.png')